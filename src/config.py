for _dir in [PROCESSED_DIR, PERSONAS_DIR, MODELS_DIR, INSTRUCTION_DIR, OUTPUTS_DIR, LOGS_DIR]:
    os.makedirs(_dir, exist_ok=True)

# ─────────────────────────────────────────────────────────────────────────────
# COLUMN DEFINITIONS
# ─────────────────────────────────────────────────────────────────────────────
TRAVELER_ID_COL: str = "ID"
