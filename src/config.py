# ─────────────────────────────────────────────────────────────────────────────
# DATA SELECTION
# Options: "SP_ONLY" | "RP_ONLY" | "BOTH"
#   SP_ONLY → Stated Preference (hypothetical scenarios, clean variation)
#   RP_ONLY → Revealed Preference (actual observed trips, real behaviour)
#   BOTH    → All trips combined (maximum data volume)
# ─────────────────────────────────────────────────────────────────────────────
DATA_TYPE: str = "SP_ONLY"

# ─────────────────────────────────────────────────────────────────────────────
# PATHS  (relative to project root; created automatically if missing)
# ─────────────────────────────────────────────────────────────────────────────
