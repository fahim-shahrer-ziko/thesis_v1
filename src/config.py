# ─────────────────────────────────────────────────────────────────────────────
# VALIDATION HELPER
# ─────────────────────────────────────────────────────────────────────────────
def validate_config() -> bool:
    """
    Check all critical settings before pipeline starts.
    Prints warnings for fixable issues, raises for blocking ones.
    """
    import urllib.request

    ok = True

    # Check DATA_TYPE
    if DATA_TYPE not in ["SP_ONLY", "RP_ONLY", "BOTH"]:
        print(f"  [ERROR] DATA_TYPE='{DATA_TYPE}' is invalid. Use SP_ONLY | RP_ONLY | BOTH")
        ok = False

    # Check Ollama server is reachable
    try:
        tags_url = OLLAMA_BASE_URL.rstrip("/v1").rstrip("/") + "/api/tags"
        urllib.request.urlopen(tags_url, timeout=3)
        print(f"  [OK]    Ollama server reachable at {OLLAMA_BASE_URL}")
    except Exception:
        print(f"  [WARN]  Ollama server not detected at {OLLAMA_BASE_URL}")
        print("          Install: https://ollama.com/download")
        print(f"          Pull  : ollama pull {OLLAMA_MODEL}")
        print("          Start : ollama serve")

    # Check raw data file
    if not os.path.exists(DATA_PATH):
        print(f"  [ERROR] Raw data not found: {DATA_PATH}")
        ok = False
    else:
        print(f"  [OK]    Raw data found: {DATA_PATH}")

    return ok
